    public void createType(String name, double perHourCharge, double perKiloCharge, double price) {
        carType c = new carType(name, perHourCharge, perKiloCharge, price);
        ct.add(c);
    }

    public void addCars(String model, int num, boolean type) throws IOException {
        int i, j;
        for (i = 0; i < ct.size(); i++) {
            if (model.equals(ct.get(i).getName())) {
                for (j = 0; j < num; j++) {
                    car c1 = new car(car_id, type);
                    ct.get(i).car_arr.add(c1);
                    car_id++;
                }
                break;
            }
        }
        writeFile();
    }

    public void writeFile() throws IOException {
        try (ObjectOutputStream ou = new ObjectOutputStream(new FileOutputStream(fileName))) {
            ou.writeObject(car_id);
            ou.writeObject(ct);
            ou.close();
        }
    }

    public static boolean isNumeric(String str) {
        if (str == "") {
            return false;
        }
        try {
            Integer.parseInt(str);
        } catch (NumberFormatException e) {
            return false;
        }
        return true;
    }

