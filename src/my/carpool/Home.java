    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Double advance = 0.0;
        int f = 0, num;
        String model = (String) jComboBox1.getSelectedItem();
        boolean type = true;
        if (jRadioButton2.isSelected()) {
            type = true;

        } else if (jRadioButton1.isSelected()) {
            type = false;

        } else {
            JOptionPane.showMessageDialog(null, "Please choose car type! (AC/Non-AC)");
            f = 1;
        }
        if (isNumeric(jTextField3.getText())) {
            num = Integer.parseInt(jTextField3.getText());
            if (num >= 4) {

            } else {
                JOptionPane.showMessageDialog(null, "Duration must be atleast 4");
                f = 1;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Duration!");
            f = 1;
        }
        try {
            advance = Double.parseDouble(jTextField4.getText());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Advance Amount!");
            f = 1;
        }
        int flag = 0;
        if (f == 0) {
            int i, j;
            for (i = 0; i < ct.size(); i++) {
                if (model.equals(ct.get(i).getName())) {
                    for (j = 0; j < ct.get(i).car_arr.size(); j++) {
                        if (ct.get(i).car_arr.get(j).getType() == type && ct.get(i).car_arr.get(j).getCarState() == 1) {
                            JOptionPane.showMessageDialog(null, "Car Successfully Rented!\nCar ID is " + ct.get(i).car_arr.get(j).getID());
                            ct.get(i).car_arr.get(j).advance = advance;
                            DateFormat df = new SimpleDateFormat("dd/MM/yy HH:mm:ss");
                            Date dateobj = new Date();
                            String reportDate = df.format(dateobj);
                            ct.get(i).car_arr.get(j).setBorrowTime(reportDate);
                            ct.get(i).car_arr.get(j).setCarState(2);
                            ct.get(i).car_arr.get(j).rentedOut += 1;
                            flag = 1;
                            break;
                        }
                    }
                    if (flag == 0) {
                        JOptionPane.showMessageDialog(null, "No cars available!");
                    } else {
                        break;
                    }
                }
            }
            if (flag == 1) {
                try {
                    writeFile();
                } catch (IOException ex) {
                    Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
                }
                setTable1();
                setTable2();
                buttonGroup1.clearSelection();
                jTextField3.setText("");
                jTextField4.setText("");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

