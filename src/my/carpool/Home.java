    public void setTable2() {
        int i, j;
        DefaultTableModel tb = (DefaultTableModel) jTable7.getModel();
        int row = jTable7.getRowCount();
        for (i = 0; i < row; i++) {
            tb.removeRow(0);
        }
        Object[] rowData = new Object[4];
        for (i = 0; i < ct.size(); i++) {
            rowData[0] = ct.get(i).getName();
            rowData[1] = ct.get(i).car_arr.get(0).getID();
            if (ct.get(i).car_arr.get(0).getType()) {
                rowData[2] = "AC";
            } else {
                rowData[2] = "Non-AC";
            }
            switch (ct.get(i).car_arr.get(0).getCarState()) {
                case 1:
                    rowData[3] = "Available";
                    break;
                case 2:
                    rowData[3] = "Rented Out";
                    break;
                case 3:
                    rowData[3] = "Out for Repair";
                    break;
                default:
                    break;
            }
            tb.addRow(rowData);
            for (j = 1; j < ct.get(i).car_arr.size(); j++) {
                rowData[0] = "";
                rowData[1] = ct.get(i).car_arr.get(j).getID();
                if (ct.get(i).car_arr.get(j).getType()) {
                    rowData[2] = "AC";
                } else {
                    rowData[2] = "Non-AC";
                }
                switch (ct.get(i).car_arr.get(j).getCarState()) {
                    case 1:
                        rowData[3] = "Available";
                        break;
                    case 2:
                        rowData[3] = "Rented Out";
                        break;
                    case 3:
                        rowData[3] = "Out for Repair";
                        break;
                    default:
                        break;
                }
                tb.addRow(rowData);
            }
        }
    }

    public void setTable1() {
        int i, j;
        DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getRowCount();
        for (i = 0; i < row; i++) {
            tb.removeRow(0);
        }
        int ac, nonac;
        for (i = 0; i < ct.size(); i++) {
            ac = 0;
            nonac = 0;
            for (j = 0; j < ct.get(i).car_arr.size(); j++) {
                if (ct.get(i).car_arr.get(j).getCarState() == 1) {
                    if (ct.get(i).car_arr.get(j).getType()) {
                        ac++;
                    } else {
                        nonac++;
                    }
                }
            }
            Object[] rowData = new Object[]{ct.get(i).getName(), nonac, ac, ct.get(i).getPerHourCharge(), ct.get(i).getPerKiloCharge()};
            tb.addRow(rowData);
        }
    }

