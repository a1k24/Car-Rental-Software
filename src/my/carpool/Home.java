    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        int i, id = 0, j, f = 0, meterReading = 0, hours = 0;
        double fuel = 0.0, cost, hourCost, kiloCost;
        if (isNumeric(jTextField10.getText())) {
            id = Integer.parseInt(jTextField10.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Invalid ID!");
            f = 1;
        }
        if (isNumeric(jTextField2.getText())) {
            meterReading = Integer.parseInt(jTextField2.getText());
        } else {
            JOptionPane.showMessageDialog(null, "Invalid Meter Reading!");
            f = 1;
        }
        try {
            fuel = Double.parseDouble(jTextField1.getText());
            if (fuel <= 0) {
                f = 1;
                JOptionPane.showMessageDialog(null, "Invalid Fuel consumption value!");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Fuel consumption value!");
            f = 1;
        }
        DateFormat df = new SimpleDateFormat("dd/MM/yy HH:mm:ss");
        Date curr = new Date();
        int flag = 0;
        if (f == 0) {
            for (i = 0; i < ct.size(); i++) {
                for (j = 0; j < ct.get(i).car_arr.size(); j++) {
                    if (ct.get(i).car_arr.get(j).getID() == id) {
                        flag = 1;
                        if (ct.get(i).car_arr.get(j).getCarState() == 2) {
                            if (meterReading > ct.get(i).car_arr.get(j).distanceRun) {
                                try {
                                    hours = (int) (curr.getTime() - df.parse(ct.get(i).car_arr.get(j).getBorrowTime()).getTime());
                                    hours = hours / (60 * 60 * 1000);
                                } catch (ParseException ex) {
                                    Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
                                }
                                if (hours <= 4) {
                                    hourCost = 4 * ct.get(i).getPerHourCharge();
                                } else {
                                    hourCost = hours * ct.get(i).getPerHourCharge();
                                }
                                if (ct.get(i).car_arr.get(j).getType()) {
                                    hourCost += hours * 50;
                                }
                                kiloCost = (meterReading - ct.get(i).car_arr.get(j).distanceRun) * ct.get(i).getPerKiloCharge();
                                cost = ((kiloCost >= hourCost) ? kiloCost : hourCost);
                                if (cost < ct.get(i).car_arr.get(j).advance) {
                                    JOptionPane.showMessageDialog(null, "Car Successfully Returned! Refunded Rs." + (ct.get(i).car_arr.get(j).advance - cost));
                                } else if (cost > ct.get(i).car_arr.get(j).advance) {
                                    JOptionPane.showMessageDialog(null, "Car Successfully Returned! Due Rs." + (cost - ct.get(i).car_arr.get(j).advance));
                                } else {
                                    JOptionPane.showMessageDialog(null, "Car Successfully Returned!");
                                }
                                ct.get(i).car_arr.get(j).distanceRun = meterReading;
                                ct.get(i).car_arr.get(j).fuelConsumed += fuel;
                                ct.get(i).car_arr.get(j).hoursRun += hours;
                                ct.get(i).car_arr.get(j).revenue += cost;
                                ct.get(i).car_arr.get(j).setCarState(1);
                                break;
                            } else {
                                JOptionPane.showMessageDialog(null, "Invalid Meter Reading!");
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Invalid ID!");
                        }

                    }
                }
            }
            if (flag == 0) {
                JOptionPane.showMessageDialog(null, "Invalid ID!");
            }
            try {
                writeFile();
            } catch (IOException ex) {
                Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextField10.setText("");
            jTextField2.setText("");
            jTextField1.setText("");
            setTable1();
            setTable2();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

